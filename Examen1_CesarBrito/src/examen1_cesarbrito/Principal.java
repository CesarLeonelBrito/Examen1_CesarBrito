    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        try {
            if (tabla_sacar.getSelectedRow() >= 0) {
                DefaultTableModel x = (DefaultTableModel) tabla_sacar.getModel();
                boolean posible = false;
                String like = (String) x.getValueAt(tabla_sacar.getSelectedRow(), 3);
                int code = Integer.parseInt(tf_codeStudent.getText());
                for (int j = 0; j < personas.size(); j++) {
                    Persona p = personas.get(j);
                    if (p.getCode() == code && p.getLibrosPrestados().size() < 5 && like.equals("disponible")) {
                        posible = true;
                    }
                }
                if (posible == true) {
                    int num = (int) x.getValueAt(tabla_sacar.getSelectedRow(), 0);
                    String estado = (String) x.getValueAt(tabla_sacar.getSelectedRow(), 3);
                    for (int i = 0; i < libros.size(); i++) {
                        Libro l = libros.get(i);
                        if (l.getIsbn() == num) {
                            l.setEstado("Prestado");
                            l.setCodePersona(code);
                            for (int j = 0; j < personas.size(); j++) {
                                Persona p = personas.get(j);
                                if (p.getCode() == code) {
                                    p.getLibrosPrestados().add(l);
                                }
                            }
                        }
                    }
                    x.setValueAt("Prestado", tabla_sacar.getSelectedRow(), 3);
                    tabla_libros.setModel(x);
                    JOptionPane.showMessageDialog(null, "Se ha sacado exitosamente");
                } else {
                    JOptionPane.showMessageDialog(null, "Error! No es posible");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error! No es posible");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error");
        }
    }//GEN-LAST:event_jButton3MouseClicked

