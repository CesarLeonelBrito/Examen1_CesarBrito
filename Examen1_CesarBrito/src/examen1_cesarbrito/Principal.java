    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        int year, isbn;
        int code = 0000;
        String estado = "disponible";
        String autor, titulo, genero, editorial, idioma;
        Date fecha;

        try {
            boolean esAdmin = false;
            int codelol = Integer.parseInt(tf_codeAdmin.getText());
            for (int i = 0; i < personas.size(); i++) {
                if (personas.get(i).getCode() == codelol) {
                    if (personas.get(i).isAdmin() == true) {
                        esAdmin = true;
                    }
                }
            }
            if (esAdmin == true) {
                year = Integer.parseInt(tf_year.getText());
                isbn = Integer.parseInt(tf_isbn.getText());
                autor = tf_autor.getText();
                titulo = tf_titulo.getText();
                genero = tf_genero.getText();
                editorial = tf_editorial.getText();
                idioma = tf_idioma.getText();
                fecha = dc_fecha.getDate();

                Libro x = new Libro(autor, titulo, genero, editorial, idioma, year, isbn, fecha, estado, codelol);
                libros.add(new Libro(autor, titulo, genero, editorial, idioma, year, isbn, fecha, estado, code));
                tf_year.setText("");
                tf_isbn.setText("");
                tf_autor.setText("");
                tf_titulo.setText("");
                tf_genero.setText("");
                tf_editorial.setText("");
                tf_idioma.setText("");
                tf_codeAdmin.setText("");
                dc_fecha.setDate(new Date());
                JOptionPane.showMessageDialog(this, "Se agrego exitosamente");

                Object[] nuevolibro = {x.getIsbn(), x.getTitulo(), x.getAutor(), x.getEstado()};
                DefaultTableModel t = (DefaultTableModel) tabla_libros.getModel();
                t.addRow(nuevolibro);
                tabla_libros.setModel(t);
                DefaultTableModel q = (DefaultTableModel) tabla_sacar.getModel();
                q.addRow(nuevolibro);
                tabla_sacar.setModel(q);

                Object[] newbook = {x.getAutor(), x.getTitulo(), x.getGenero(), x.getEditorial(), x.getIsbn(), x.getEstado()};
                DefaultTableModel m = (DefaultTableModel) tabla_buscar.getModel();
                m.addRow(newbook);
                tabla_buscar.setModel(m);

            } else {
                JOptionPane.showMessageDialog(this, "El codigo no pertenece a un administrador");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error y no se guardaron los datos");
        }
        //System.out.println(libros);
    }//GEN-LAST:event_jButton1MouseClicked

